package all;

public class Problem13Vertex {
	private int to;
	private int price;

	public Problem13Vertex(int to, int price) {
		this.to = to;
		this.price = price;
	}

	public int getTo() {
		return to;
	}

	public void setTo(int to) {
		this.to = to;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
}
